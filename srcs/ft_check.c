#include "ft.h"

int        ft_tryo(char c)
{
    return ((c >= 40 && c <= 43) || c == 45 || c == 47 || c == 37);
}

int        ft_ver(char c)
{
    return (c >= '0' && c <= '9');
}

int		ft_check(char c)
{
	return (ft_tryo(c) || ft_ver(c));
}
